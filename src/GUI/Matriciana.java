    public Cella[][] getMatrix() {
        return matrix;
    }

    public void drawMatrix(Graphics g) {

        for (int i = 0; i < RIGA; i++) {

            for (int j = 0; j < COLONNA; j++) {

                switch (matrix[i][j].eUn()) {
                    case 1:
                        g.setColor(Color.red);
                        g.fillRect(matrix[i][j].getX(), matrix[i][j].getY(), matrix[i][j].getW(), matrix[i][j].getH());
                        break;
                    case 0:
                        g.setColor(Color.green);
                        g.fillRect(matrix[i][j].getX(), matrix[i][j].getY(), matrix[i][j].getW(), matrix[i][j].getH());
                        break;
                    case 2:
                        g.setColor(Color.yellow); 
                        g.fillRect(matrix[i][j].getX(), matrix[i][j].getY(), matrix[i][j].getW(), matrix[i][j].getH());
                        break;
                    default:
                        break;
                }

                g.setColor(Color.black);
                 g.drawRect(matrix[i][j].getX(), matrix[i][j].getY(), matrix[i][j].getW(), matrix[i][j].getH());


         
            }

        }
    }

